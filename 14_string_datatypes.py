"""

1. A string is a collection of one or more characters put in a single quote, double quote or triple quote
2. Multi-line string can be denoted using triple quote
3. it is immutable
"""

s1 = "this is string 1"
s2 = 'this is string 2'
s3 = """ this is string 3"""
s4 = ''' this is string 4'''
s5 = " hello@1234"
s6 = '10'

print(s1, type(s1))
print(s2, type(s2))
print(s3, type(s3))
print(s4, type(s4))
print(s5, type(s5))
print(s6, type(s6))