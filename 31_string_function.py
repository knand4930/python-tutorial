"""
Function of String

1. Lower, upper, title, capitalize
2. Lower : all the character are lower value
3. upper : all the character are upper value
4. title : all the title value are capital
5. capitalize : all the string value to start with capital


"""


a = "Hello World"
b = a.lower()
c = a.upper()
d = a.title()
e = a.capitalize()
print(b)
print(c)
print(d)
print(e)
