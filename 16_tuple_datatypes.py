"""
1. Tuple is an ordered sequence of items same as a list
2. It is defined within parentheses () where items are separated by commas
3. sign = () - minimum 2 values


"""

t1 = (1, 2, 3, 4, 5)
t2 = (1, 2, 3, 4.5, "hello world", 'helo@1234')
t3 = (19)
t4 = (29,)

print(t1, type(t1))
print(t2, type(t2))
print(t3, type(t3))
print(t4, type(t4))
