"""

1. min() : Minimum value in tuple
2. max() : Maximum Value in tuple
3. count() : count the value in tuple
4. index() :  index the value and show the results
5. sum() : only the value of integer or float not another third party values

"""

t = (2,32,4,6,4,5,7,6,54,3,56,54,3,90)

print(t)
print(t.index(90))
print(t.count(6))
print(sum(t))
print(min(t))
print(max(t))